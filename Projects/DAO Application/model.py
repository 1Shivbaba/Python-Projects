class Employee:
    def setid(self, empid):
        self.empid = empid
    def getid(self):
        return self.empid

    def setname(self, empname):
        self.empname = empname
    def getname(self):
        return self.empname

    def setdept(self, empdept):
        self.empdept = empdept
    def getdept(self):
        return self.empdept

    def setsalary(self, empsal):
        self.empsal = empsal
    def getsalary(self):
        return self.empsal